import cvxpy as cp
import numpy as np 

x = cp.Variable(1)
# y = cp.Variable(1)
z = cp.Variable(1)
# w = cp.Variable(1)

obj = cp.Minimize( \
    cp.abs(244849928*x + 97470486*0 + 301023131*z + 30738760130*0 - 83) + \
    cp.abs(482884030*x + 202430630*0 + 613212426*z + 28985995302*0 - 84) + \
    cp.abs(732509330*x + 115148096*0 + 644271253*z + 89181915044*0 - 184) + \
    cp.abs(176062809*x + 81671535*0 + 223252703*z + 26637257564*0 - 69) + \
    cp.abs(402143453*x + 168697072*0 + 508117320*z + 28323359643*0 - 81) + \
    cp.abs(242263962*x + 112196304*0 + 298299864*z + 27739254542*0 - 74) + \
    cp.abs(174998729*x + 81233114*0 + 222107770*z + 26638426974*0 - 70) + \
    cp.abs(260088427*x + 118124409*0 + 333509515*z + 27558067909*0 - 73) + \
    cp.abs(994420*x + 927433*0 + 21852*z + 214652819*0 - 0.187) + \
    cp.abs(694741475*x + 95560*0 + 657902930*z + 6895842484*0 - 246) + \
    cp.abs(32339330*x + 20552845*0 + 32607131*z + 170361327*0 - 27) + \
    cp.abs(27796*x + 1756*0 + 18716*z + 2165715*0 - 0.092285))

# con = [x>=0, y>=0, z>=0, w>=0]
con = [x>=0, z>=0]

prob = cp.Problem(obj,con)

# prob.solve(verbose=True)
prob.solve()

